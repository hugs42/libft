#include "libft.h"

double	ft_sqrt(double x)
{
	double	nb;

	nb = 1;
	if (x > 0)
	{
		while (nb * nb <= x)
		{
			if (nb * nb == x)
				return (nb);
			nb++;
		}
	}
	return (0);
}
